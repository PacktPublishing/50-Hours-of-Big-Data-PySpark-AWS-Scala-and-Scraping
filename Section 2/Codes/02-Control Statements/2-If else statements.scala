import scala.io.StdIn.readLine

object Main {
  def main(args: Array[String]): Unit = {
  var x = readLine("Enter number 1");
  var num1 = x.toInt;
  var y = readLine("Enter number 2");
  var num2 = y.toInt;



  if (num1 > num2)
  {
    println("Number 1 is greater");

  }    
  else
  {
    println("Number 2 is greater");    
  }


  }
}
